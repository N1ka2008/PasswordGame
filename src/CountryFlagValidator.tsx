function CountryFlagValidator({ password }: Props) {
  const [selectedCountry, setSelectedCountry] = useState('');
  const [flagUrl, setFlagUrl] = useState('');
  const [isValid, setIsValid] = useState(false);

  useEffect(() => {
    const random = countries[Math.floor(Math.random() * countries.length)];
    //eslint-disable-next-line react-hooks/set-state-in-effect
    setSelectedCountry(random);

    setFlagUrl(`https://flagsapi.com/${random}/flat/64.png`);
  }, []);

  useEffect(() => {
    if (!selectedCountry) return;

    const valid = password
      .toUpperCase()
      .includes(selectedCountry.toUpperCase());
    //eslint-disable-next-line react-hooks/set-state-in-effect
    setIsValid(valid);
  }, [password, selectedCountry]);

  return (
    <div style={{ marginTop: 20 }}>
      <h4>Validace podle zeme</h4>

      {flagUrl && <img src={flagUrl} alt='flag' style={{ width: 70 }} />}
      <p>{isValid ? 'Heslo obsahuje zkratku' : `Heslo neobsahuje zkratku`}</p>
    </div>
  );
}

export default CountryFlagValidator;
